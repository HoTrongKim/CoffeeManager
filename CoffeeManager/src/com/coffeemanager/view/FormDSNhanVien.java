    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // Get data from input fields
        String maNV = txt_maNV.getText().trim();
        String fullName = txt_Ten.getText().trim();
        String sex = rdobtn_Nam.isSelected() ? "Nam" : rdobtn_Nu.isSelected() ? "Nữ" : rdobtn_Khac.isSelected() ? "Khác" : "";
        String chucVu = cbx_chucVu.getSelectedItem().toString();
        String taiKhoan = txt_TaiKhoan.getText().trim();
        String matKhau = txt_matKhau.getText().trim();
        String soDienThoai = txt_sdt.getText().trim();
        String email = txt_email.getText().trim();

        // Validate input
        if (maNV.isEmpty() || fullName.isEmpty() || sex.isEmpty() || chucVu.isEmpty() || taiKhoan.isEmpty() || matKhau.isEmpty() || soDienThoai.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "⚠️ Vui lòng nhập đầy đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Update employee in the database
        boolean success = connect.updateEmployee(maNV, fullName, sex, chucVu, taiKhoan, matKhau, soDienThoai, email);
        if (success) {
            JOptionPane.showMessageDialog(this, "🎉 Cập nhật nhân viên thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadEmployeeData(); // Refresh table
            clearEditFields(); // Clear edit fields after update
        } else {
            JOptionPane.showMessageDialog(this, "❌ Lỗi khi cập nhật nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_suaActionPerformed


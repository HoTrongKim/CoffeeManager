    /**
     * Load employee data from the database into tbl_dsNhanVien
     */
    private void loadEmployeeData() {
        DefaultTableModel model = (DefaultTableModel) tbl_dsNhanVien.getModel();
        model.setRowCount(0); // Clear existing rows

        List<Employees> employees = connect.selectAllEmployees();
        for (Employees emp : employees) {
            model.addRow(new Object[]{
                emp.getMaNV(),
                emp.getFullName(),
                emp.getSex(),
                emp.getChucVu(),
                emp.getTaiKhoan(),
                emp.getMatKhau(),
                emp.getSoDienThoai(),
                emp.getEmail()
            });
        }
    }

    /**
     * Set up listener for table row selection to load data into edit fields
     */
    private void setupTableSelectionListener() {
        tbl_dsNhanVien.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting() && tbl_dsNhanVien.getSelectedRow() != -1) {
                    int selectedRow = tbl_dsNhanVien.getSelectedRow();
                    txt_maNV.setText(tbl_dsNhanVien.getValueAt(selectedRow, 0).toString());
                    txt_Ten.setText(tbl_dsNhanVien.getValueAt(selectedRow, 1).toString());

                    // Clear previous radio button selection
                    buttonGroup1.clearSelection();

                    // Set radio button for gender
                    String sex = tbl_dsNhanVien.getValueAt(selectedRow, 2).toString();
                    if ("Nam".equalsIgnoreCase(sex)) {
                        rdobtn_Nam.setSelected(true);
                    } else if ("Nữ".equalsIgnoreCase(sex)) {
                        rdobtn_Nu.setSelected(true);
                    } else if ("Khác".equalsIgnoreCase(sex)) {
                        rdobtn_Khac.setSelected(true);
                    }
                    // If sex is not "Nam", "Nữ", or "Khác", no radio button is selected

                    cbx_chucVu.setSelectedItem(tbl_dsNhanVien.getValueAt(selectedRow, 3).toString());
                    txt_TaiKhoan.setText(tbl_dsNhanVien.getValueAt(selectedRow, 4).toString());
                    txt_matKhau.setText(tbl_dsNhanVien.getValueAt(selectedRow, 5).toString());
                    txt_sdt.setText(tbl_dsNhanVien.getValueAt(selectedRow, 6).toString());
                    txt_email.setText(tbl_dsNhanVien.getValueAt(selectedRow, 7).toString());
                }
            }
        });
    }


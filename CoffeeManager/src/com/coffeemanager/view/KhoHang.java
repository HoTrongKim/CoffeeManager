    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_DangKyLichLam.getModel();
        String maHang = jTextField1.getText().trim();
        String tenHang = jTextField3.getText().trim();
        String gia = jTextField4.getText().trim();
        String soLuong = jTextField5.getText().trim();
        String ghiChu = jTextField2.getText().trim();

        if (maHang.isEmpty() || tenHang.isEmpty() || gia.isEmpty() || soLuong.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int g = Integer.parseInt(gia);
            int sl = Integer.parseInt(soLuong);
            model.addRow(new Object[]{maHang, tenHang, g, sl, ghiChu});
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá và Số lượng phải là số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedRow = tbl_DangKyLichLam.getSelectedRow();
        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) tbl_DangKyLichLam.getModel();
            try {
                String maHang = jTextField1.getText().trim();
                String tenHang = jTextField3.getText().trim();
                int gia = Integer.parseInt(jTextField4.getText().trim());
                int soLuong = Integer.parseInt(jTextField5.getText().trim());
                String ghiChu = jTextField2.getText().trim();

                model.setValueAt(maHang, selectedRow, 0);
                model.setValueAt(tenHang, selectedRow, 1);
                model.setValueAt(gia, selectedRow, 2);
                model.setValueAt(soLuong, selectedRow, 3);
                model.setValueAt(ghiChu, selectedRow, 4);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Giá và Số lượng phải là số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sửa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tbl_DangKyLichLam.getSelectedRow();
        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) tbl_DangKyLichLam.getModel();
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xoá.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_DangKyLichLam.getModel();
        try (FileWriter csvWriter = new FileWriter("khohang.csv")) {
            csvWriter.append("Mã Hàng,Tên Hàng,Giá,Số Lượng,Ghi Chú\n");
            for (int i = 0; i < model.getRowCount(); i++) {
                csvWriter.append(model.getValueAt(i, 0).toString()).append(",")
                        .append(model.getValueAt(i, 1).toString()).append(",")
                        .append(model.getValueAt(i, 2).toString()).append(",")
                        .append(model.getValueAt(i, 3).toString()).append(",")
                        .append(model.getValueAt(i, 4).toString()).append("\n");
            }
            JOptionPane.showMessageDialog(this, "Dữ liệu đã được lưu vào file khohang.csv", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi lưu file: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

